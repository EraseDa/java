package day03;

public class ifEx01 {

	public static void main(String[] args) {
		/* ~하면 ...한다 
		 * ~ : 조건식
		 * ... : 실행문
		 * if(조건식){실행문;}
		 * */
		//정수 num가 0보다 크면 양수라고 출력하는 코드를 작성
		
		int num =-50;
		if (num > 0) {System.out.println(num + "은 양수입니다.");}
		
		if (num == 0) {System.out.println(num + "은 0입니다.");}
		if (num < 0){System.out.println(num + "은 음수입니다.");}
		}
		
	}


